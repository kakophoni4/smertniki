def test_clean_company():
    text = """
    сведения о юридическом лице ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ "ЧИСТАЯ" ОГРН 1000000000001
    ИНН юридического лица 7700000000
    Адрес юридического лица 101000, Г.МОСКВА, УЛ. ТВЕРСКАЯ, Д. 1
    Сведения о лице, имеющем право без доверенности
    ИВАНОВ ИВАН ИВАНОВИЧ Должность ГЕНЕРАЛЬНЫЙ ДИРЕКТОР
    Сведения об учредителях юридического лица
    """
    snap = parse_vypiska_text(text, "1000000000001")
    assert snap.has_any_issue() is False
    assert snap.inn == "7700000000"
