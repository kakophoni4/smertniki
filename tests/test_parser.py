"""Тесты разбора выписки ЕГРЮЛ (текст из PDF)."""

from pathlib import Path

from app.services.egrul import parse_vypiska_text

PROBE = Path(__file__).resolve().parent.parent / "data_probe"


def test_prorva_from_real_extract():
    path = PROBE / "vypiska.txt"
    if not path.exists():
        # минимальный синтетический фрагмент по реальной формулировке ФНС
        text = """
        ВЫПИСКА из Единого государственного реестра юридических лиц
        сведения о юридическом лице ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ "ПРОРВА" ОГРН 1227700761001
        ИНН юридического лица 9726027672
        Адрес юридического лица 117639, Г.МОСКВА, ПР-КТ БАЛАКЛАВСКИЙ, Д. 2, К. 4
        9 Дополнительные сведения сведения недостоверны (результаты проверки достоверности содержащихся в ЕГРЮЛ сведений о юридическом лице)
        Сведения о лице, имеющем право без доверенности действовать от имени юридического лица
        ХОДУЛИН ВИКТОР ВЛАДИМИРОВИЧ Должность ГЕНЕРАЛЬНЫЙ ДИРЕКТОР
        33 Дополнительные сведения сведения недостоверны (результаты проверки достоверности содержащихся в ЕГРЮЛ сведений о юридическом лице)
        Сведения об учредителях юридического лица
        Сведения о состоянии юридического лица 20 Состояние Регистрирующим органом принято решение о предстоящем исключении юридического лица из ЕГРЮЛ (наличие в ЕГРЮЛ сведений о юридическом лице, в отношении которых внесена запись о недостоверности)
        """
    else:
        text = path.read_text(encoding="utf-8")

    snap = parse_vypiska_text(text, "1227700761001")
    assert snap.unreliable_address is True
    assert snap.unreliable_director is True
    assert snap.is_liquidating is True
    assert snap.inn in (None, "9726027672") or snap.inn == "9726027672"


